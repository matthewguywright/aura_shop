<h2>Color &amp; Sound Healing</h2>
<p>Color and sound are both forms of energy. As the vibration of     sound becomes higher and lighter, the sound is transmuted into color. Each of     our seven chakras vibrates at a different speed, translating into color and     sound.</p>
<p>Steven Halpern, a major leader in Sound Therapy, explains the     relationship between sound and color in his book, "Sound Health - The Music     and Sound That Makes Us Whole."</p>
<p>He says that, "Vibrations at 1000 cycles per second are     easily audible. If you double the vibrations to 2000 cycles per second, that     is one octave higher. If you double it again to 4000 cycles per second, that     is another octave. A normal piano spans a bit more than seven octaves. If, hypothetically,     we could extend the piano keyboard another 35 to 50 octaves higher, the keys     at the higher end would produce colors, rather than audible sounds, when played."</p>
<p>Both color and sound affect our emotions and energetic wellness.     When Light and Music are used together, our energy centers - chakras are especially     activated resulting in a strong impact and positive change upon our aura.</p>
