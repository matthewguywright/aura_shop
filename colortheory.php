<h2>Color Theory</h2>
<p>Primary Colors:<br />
  <br />
    <span class="style1">RED</span><br>
Vital Energy, Dynamic Will, Passion, Grounding
<p><span class="style2">YELLOW</span><br>
  Intellect, Focus, Power, Humor</p>
<p><span class="style3">BLUE</span><br>
  Sensitivity, Intuition, Expression, Truth</p>
<p>
   
 
  Secondary Colors:<br />
  <strong><span class="style6">ORANGE</span> (RED &amp; YELLOW)</strong><br>
  Oranges have the dynamic will power of red and the clarity and focus of <br>
  yellow.</p>
<p><strong><span class="style5">GREEN</span> (YELLOW &amp; BLUE)</strong><br>
  Greens have the mental attributes of yellow and the sensitivity &amp; expression <br>
  of blue.</p>
<p><strong><span class="style11">VIOLET</span> (RED &amp; BLUE)</strong><br>
  Violets have the charismatic energy of red and the sensitivity &amp; spirituality     of blue.</p>
<p><strong><span class="style10">INDIGO</span> (REDDISH DEEP BLUE)</strong><br>
  Indigos have the dynamic energy and passion of red with a lot of <br>
  sensitivity of blue.<br>
  <br>

  Complementary Colors:<br />
  <span class="style4">RED</span> - <span class="style5">GREEN</span><br>
  <span class="style6">ORANGE</span> - <span class="style7">BLUE</span><br>
  <span class="style8">YELLOW</span> - <span class="style11">VIOLET</span> </p>
<p>Working with complementary colors can balance and improve your     energy system tremendously. For example: for <span class="style6">ORANGE</span>, use either Orange to increase     or exemplify creative ideas and productive energies, or use <span class="style7">Blue</span> to calm and     to express your creativity.</p>
